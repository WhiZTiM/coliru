#include <iostream>
#include <cstdint>

int main()
{
    const uint64_t x = 0b0000'0000'0000'0000'1000'0000'0000'0000'0000'0000'0000'0010'1110'0000'0000'0010ULL;
    //                  |---------------------------------------|---------------------------------------|
    //                  |-------------------|-------------------|-------------------|-------------------|
    //                  |---------|---------|---------|---------|---------|---------|---------|---------|
    //                  |----|----|----|----|----|----|----|----|----|----|----|----|----|----|----|----|
    int n = 0;
    if (x > 0) {
        n = 64 - __builtin_clzll(x);
    }
    
    for (int i = 0; i < n; i++) {
        
        const bool is_set = x & (uint64_t(1) << i);
        
        std::cout << "Bit " << i << " is ";
        if (!is_set)
            std::cout << "not ";
        std::cout << "set\n";
    }
}
