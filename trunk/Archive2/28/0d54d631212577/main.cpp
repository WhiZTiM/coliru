#include <iostream>

int main()
{
    
    int i =4;
    std::cout << " i "  << i << std::endl;
    std::cout << " ~i "  << ~i << std::endl;
    std::cout << " -i "  << -i << std::endl;
    std::cout << " ~(-i) "  << ~-i << std::endl;
    std::cout << " ~-i "  << ~(-i) << std::endl;
    
}
